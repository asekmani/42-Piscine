int	ft_iterative_factorial(int nb)
{
	int	i;

	if (nb == 0 || nb == 1)
		return (1);
	if (nb < 0)
		return (0);
	i = nb * (nb - 1);
	while (nb > 2)
	{
		i = i * (nb - 2);
		nb = nb - 1;
	}
	return (i);
}
